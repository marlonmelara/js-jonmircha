// Curso JavaScript: 9. undefined, null & NaN
// https://youtu.be/dux1Uv2eLfw

/* undefined indica que no se ha inicializado una variable
y que el valor esta ausente*/
let indefinida;
console.log(indefinida); // undefined

/* null es un valor especial que indica la ausencia
de un valor*/
let nulo = null;
console.log(null); // null

// Nan - Not a Number
let noEsUnNumero = "hola" * 3.7;
console.log(noEsUnNumero); // NaN
